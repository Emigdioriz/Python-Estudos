import random


def jogar():

    imprime_mensagem_abertura()

    palavra_secreta = carrega_palavra_secreta()
    # usando um for dentro da inicialização da lista para criar _ conforme o comprimento da palavra_secreta
    letras_acertadas = ["_" for letra in palavra_secreta]

    enforcou = False
    acertou = False
    erros = 0

    print(letras_acertadas)

    # enquando nao enforcou e nao acertou
    while (not enforcou and not acertou):

        chute = input("Qual letra? ")
        chute = chute.strip().lower()
        # strip retira  os espaços do input (caso haja), lower transforma todas as letras em minúsculas

        if (chute in palavra_secreta):
            marca_chute_correto(chute, letras_acertadas, palavra_secreta)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if (acertou):
        imprime_mensagem_vencedor()
    else:
        imprime_mensagem_perdedor(palavra_secreta)


# funções
def imprime_mensagem_abertura():
    print("\n******************************")
    print("Bem vindo ao jogo da Forca")
    print("******************************")


def carrega_palavra_secreta():
    # abrindo o arquivo txt
    arquivo = open("palavras.txt", "r")
    palavras = []

    # selecionando as palavras do arquivo txt e as colocando em uma lista chamada palavras
    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    i = random.randrange(0, len(palavras))
    palavra_secreta = palavras[i]
    return palavra_secreta


def marca_chute_correto(chute, letras_acertadas, palavra_secreta):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index = index + 1


